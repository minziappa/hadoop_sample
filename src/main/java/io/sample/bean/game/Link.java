package io.sample.bean.game;

public class Link {

	// attributes:rel
	private String rel;
	// attributes:type
	private String type;
	// attributes:href
	private String href;
	public String getRel() {
		return rel;
	}
	public void setRel(String rel) {
		this.rel = rel;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getHref() {
		return href;
	}
	public void setHref(String href) {
		this.href = href;
	}

}
